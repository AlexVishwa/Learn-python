a = 4
b = 3
print(f'{a} + {b} = {a +b}')        # 4 + 3 = 7
print(f'{a} - {b} = {a - b}')       # 4 - 3 = 1
print(f'{a} * {b} = {a * b}')       # 4 * 3 = 12
print(f'{a} / {b} = {a / b:.2f}')   # 4 / 3 = 1.33
print(f'{a} % {b} = {a % b}')       # 4 % 3 = 1
print(f'{a} // {b} = {a // b}')     # 4 // 3 = 1
print(f'{a} ** {b} = {a ** b}')     # 4 ** 3 = 64